using namespace std;

char ss[1003];
char tt[1003];

int main(){
    int slen; int tlen;
    scanf("%d %d",&slen,&tlen);
    scanf("%s %s",ss,tt);
    int startidx = -1;
    int mxfnd = 0;
    for(int i=0; (i+slen-1)<tlen;++i){
        int cnt = 0;
        int st = 0;
        for(int j=i; j<=(i+slen-1);++j){
            if(tt[j]==ss[st]){
                cnt++;
            }
            st++;
        }
        if(cnt>mxfnd){
            mxfnd = cnt;
            startidx = i;
        }
    }
    cout<<slen-mxfnd<<"\n";
    int idx = 0;
    //cout<<startidx<<"\n";
    if(startidx==-1)startidx++;
    for(int i = startidx; i<= (startidx+slen-1); ++i){
        if(tt[i]!=ss[idx]){
            cout<<idx+1<<" ";
        }
        idx++;
    }
    if((slen-mxfnd)!=0)cout<<"\n";
}
