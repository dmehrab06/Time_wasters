unsigned long long zeroCount(unsigned long long k)
{
    unsigned long long zeros = 0;
    while(k) {
        zeros += (k % 10) == 0;
        k /= 10;
    }
    return zeros;
}

unsigned long long Z(unsigned long long n)
{
    if (n == 0) {
        return 0;
    }
    if (n <= 10) {
        return 1;
    }
    unsigned long long k = n/10, r = n%10;
    unsigned long long zeros = k + 10*(Z(k)-1);
    if (r > 0) {
        zeros += r*zeroCount(k) + 1;
    }
    return zeros;
}

unsigned long long totalDigits( LLI n)
{
    unsigned long long number_of_digits = 0;
    for (LLI i = 1; i <= n; i *= 10)
        number_of_digits += (unsigned long long) (n - i + 1);

    return number_of_digits;
}

