    bool bad(int f1, int f2, int f3, int slopetype, int querytype){
        __int128 lft = __int128(c[f3]-c[f1])*(m[f1]-m[f2]);
        __int128 rgt = __int128(c[f2]-c[f1])*(m[f1]-m[f3]);
        int xx = slopetype^querytype;
        if(xx)return lft>rgt; //can be lft>=rgt khetrobisesh e
        else return lft<rgt; //can be lft<=rgt khetrobisesh e
    }
    void add(LLI _m, LLI _c){
        m.push_back(_m); c.push_back(_c);
        int sz = m.size(); //m and c both are of equal size;
        while( sz>=3 && bad(sz-3,sz-2,sz-1)){
            swap(m[sz-2],m[sz-1]); swap(c[sz-2],c[sz-1]);
            m.pop_back(); c.pop_back();
            sz--;
        }
    }
