// Utility function to find GCD of 'a' and 'b'
ll gcd(ll a, ll b)
{
    if (b==0)
        return a;
    return gcd(b, a%b);
}

// Returns LCM of array elements
ll findlcm(ll arr[], int n)
{
    // Initialize result
    ll ans = arr[0];

    // ans contains LCM of arr[0],..arr[i]
    // after i'th iteration,
    for (int i=1; i<n; i++)
        ans = ( ((arr[i]*ans)) /
                (gcd(arr[i], ans)) );

    return ans;
}

